export function getPostsByTab(tab) {
    switch(tab) {
        case 'grid':
            return photoPosts;
        case 'tagged':
            return groupPosts;
        case 'story':
            return storyPosts;
        case 'repost':
            return repostPosts;
        default:
            return photoPosts;
    }
}

// ê²Œì‹œë¬¼ ê°œìˆ˜
export function getPostCount(tab = 'grid') {
    return getPostsByTab(tab).length;
}

